            When submitting a new abstract / draft paper, it is expected to follow the guidelines as listed below,
            <ul>
                <li>An abstract / paper describing essentially the same research have not been submitted to several journals to avoid multiple or redundant publication</li>
                <li>The paper is original; all the work and wording which was taken from others is appropriately cited</li>
                <li align="justify">People identified as co-authors fully qualify for authorship (significant contribution to the work, development of the design and concept of the research, data processing and analysis, writing or revising the manuscript, accountability for all aspects of the work)</li>
                <li align="justify">All the co-authors give their consent to submit the final version of the manuscript</li>
                <li align="justify">The paper should be written in correct English.</li>
               
